package Codeforces;

import java.util.Scanner;

public class ShortestPath {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int t = sc.nextInt();
        sc.nextLine();
        for (int i = 0; i < t; i++) {
            int[] a = new int[2];
            int[] b = new int[2];
            int[] f = new int[2];
            a[0] = sc.nextInt();
            a[1] = sc.nextInt();
            b[0] = sc.nextInt();
            b[1] = sc.nextInt();
            f[0] = sc.nextInt();
            f[1] = sc.nextInt();
        }
    }
}
